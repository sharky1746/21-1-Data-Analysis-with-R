#H0 : mu>=14.68 2차훈련은 평균기록을 단축시키지 않는다.
#H1 : mu<14.68 2차훈련은 평균기록을 단축시킨다.


# d.[20점] 기록 단축이 달성되었는지 검정하고 결과를 설명하시오. 단, 유의수준은 0.05로 합니다. 

